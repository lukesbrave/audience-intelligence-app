function getChannelIcon(name: string): string {
  const normalizedName = name.toLowerCase();
  for (const [key, path] of Object.entries(channelIcons)) {
    if (normalizedName.includes(key)) {
      return path;
    }
  }
  // Default icon
  return 'M8.228 9c.549-1.165 2.03-2 3.772-2 2.21 0 4 1.343 4 3 0 1.4-1.278 2.575-3.006 2.907-.542.104-.994.54-.994 1.093m0 3h.01M21 12a9 9 0 11-18 0 9 9 0 0118 0z';
}

function ChannelGrid({ channels }: ChannelGridProps) {
  // Sort by mention count
  const sortedChannels = [...channels].sort((a, b) => b.mentioned - a.mentioned);
  const maxMentions = Math.max(...channels.map(c => c.mentioned));

  return (
    <div className="grid grid-cols-2 md:grid-cols-3 lg:grid-cols-4 gap-3">
      {sortedChannels.map((channel, i) => {
        const effectiveness = channel.mentioned / maxMentions;
        const stars = Math.round(effectiveness * 5);

        return (
          <div
            key={i}
            className="bg-white rounded-lg border border-gray-200 p-4 hover:shadow-sm transition-shadow"
          >
            <div className="flex items-center gap-2 mb-2">
              <div className="w-8 h-8 rounded-lg bg-[#BBDCEF]/30 flex items-center justify-center">
                <svg className="w-4 h-4 text-[#16314C]" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d={getChannelIcon(channel.name)} />
                </svg>
              </div>
              <span className="font-medium text-gray-900 text-sm">{channel.name}</span>
            </div>

            {/* Star rating */}
            <div className="flex items-center gap-0.5 mb-1">
              {[1, 2, 3, 4, 5].map(star => (
                <svg
                  key={star}
                  className={`w-3.5 h-3.5 ${star <= stars ? 'text-amber-400' : 'text-gray-200'}`}
                  fill="currentColor"
                  viewBox="0 0 20 20"
                >
                  <path d="M9.049 2.927c.3-.921 1.603-.921 1.902 0l1.07 3.292a1 1 0 00.95.69h3.462c.969 0 1.371 1.24.588 1.81l-2.8 2.034a1 1 0 00-.364 1.118l1.07 3.292c.3.921-.755 1.688-1.54 1.118l-2.8-2.034a1 1 0 00-1.175 0l-2.8 2.034c-.784.57-1.838-.197-1.539-1.118l1.07-3.292a1 1 0 00-.364-1.118L2.98 8.72c-.783-.57-.38-1.81.588-1.81h3.461a1 1 0 00.951-.69l1.07-3.292z" />
                </svg>
              ))}
            </div>

            <p className="text-xs text-gray-500">
              {channel.mentioned === 1 ? '1 mention' : `${channel.mentioned} mentions`}
            </p>
          </div>
        );
      })}
    </div>
  );
}

